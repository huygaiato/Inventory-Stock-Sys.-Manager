package model;

/**
 *
 * @author Huy G To
 * The Part class is an abstract class which is the parent class for both Inhouse and Outsourced. contains the parameters to
 * create an object and contains the methods to get/set any of the parameters */
public abstract class Part {
    /**
     * int containing the part id
     */
    private int id;
    /**
     * string containing the part name
     */
    private String name;
    /**
     * double containing the part price
     */
    private double price;
    /**
     * int containing the part stock
     */
    private int stock;
    /**
     * int containing the minimum value
     */
    private int min;
    /**
     * int containing the maximum value
     */
    private int max;
    /**
     * Constructor for part
     */
    public Part(int id, String name, double price, int stock, int min, int max) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.stock = stock;
        this.min = min;
        this.max = max;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        int whatever = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the price
     */
    public double getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(double price) {
        this.price = price;
    }

    /**
     * @return the stock
     */
    public int getStock() {
        return stock;
    }

    /**
     * @param stock the stock to set
     */
    public void setStock(int stock) {
        this.stock = stock;
    }

    /**
     * @return the min
     */
    public int getMin() {
        return min;
    }

    /**
     * @param min the min to set
     */
    public void setMin(int min) {
        this.min = min;
    }

    /**
     * @return the max
     */
    public int getMax() {
        return max;
    }

    /**
     * @param max the max to set
     */
    public void setMax(int max) {
        this.max = max;
    }

}